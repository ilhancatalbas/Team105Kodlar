public class C10_SoruSocrative01 {

    public static void main(String[] args) {

        // Asagidaki kod calistiginda output ne olur?

        for (char i='f'; i>'a'; i--){

            System.out.print(i + " ");
            i--;
        }
                                                 // biz char olarak f'den baslayip, a'ya kadar gidecegiz. en alt satira i--; oldugu icin i'yi bir azaltacagiz.
                                                 // mesela f'yi bir azaltirsak e olur boyle devam eder


        //  f d b



    }
}
